def char_count(text: str) -> int:
    """按当前论文约定统计字数：去掉空白后计数。"""

    return len(re.sub(r"\s+", "", text))


def set_paragraph_text(paragraph, text: str) -> None:
    """
    尽量保留段落当前格式，仅替换文字内容。

    这里保留首个 run 的常见字体属性，避免因为重写文本导致明显格式漂移。
    对于用户已手工调整好的版式，我们只做最小化文本替换。
    """

    first_run = paragraph.runs[0] if paragraph.runs else None
    run_props = {}
    if first_run is not None:
        run_props = {
            "bold": first_run.bold,
            "italic": first_run.italic,
            "underline": first_run.underline,
            "font_name": first_run.font.name,
            "font_size": first_run.font.size,
        }

    for run in list(paragraph.runs):
        paragraph._p.remove(run._r)

    new_run = paragraph.add_run(text)
    if run_props:
        new_run.bold = run_props["bold"]
        new_run.italic = run_props["italic"]
        new_run.underline = run_props["underline"]
        new_run.font.name = run_props["font_name"]
        new_run.font.size = run_props["font_size"]


def parse_args() -> argparse.Namespace:
    """解析命令行参数。"""

    parser = argparse.ArgumentParser(description="把论文正文从 9207 字附近回补到约 12000 字。")
    parser.add_argument("--input", required=True, help="待处理的 DOCX 路径。")
    parser.add_argument(
        "--backup",
        help="备份文件路径；若不提供，则自动生成“_回补前备份”文件。",
    )
    return parser.parse_args()


def main() -> None:
    """执行正文回补并输出处理前后字数。"""

    args = parse_args()
    input_path = Path(args.input)
    if not input_path.exists():
        raise FileNotFoundError(f"未找到文件：{input_path}")

    backup_path = (
        Path(args.backup)
        if args.backup
        else input_path.with_name(f"{input_path.stem}_回补前备份{input_path.suffix}")
    )
    if not backup_path.exists():
        shutil.copy2(input_path, backup_path)

    doc = Document(str(input_path))
    before = sum(char_count(p.text) for p in doc.paragraphs)

    for idx, text in REBALANCE_REPLACEMENTS.items():
        if idx >= len(doc.paragraphs):
            raise IndexError(f"段落索引超出范围：{idx}")
        set_paragraph_text(doc.paragraphs[idx], text)

    after = sum(char_count(p.text) for p in doc.paragraphs)
    doc.save(str(input_path))

    print(f"backup: {backup_path}")
    print(f"before_chars: {before}")
    print(f"after_chars: {after}")


if __name__ == "__main__":
    main()
