def set_run_font(run, size: float | None = None) -> None:
    """统一补充段落字体，避免新增内容和正文风格不一致。"""

    run.font.name = "宋体"
    run._element.rPr.rFonts.set(qn("w:eastAsia"), "宋体")
    run._element.rPr.rFonts.set(qn("w:ascii"), "Times New Roman")
    run._element.rPr.rFonts.set(qn("w:hAnsi"), "Times New Roman")
    if size is not None:
        run.font.size = Pt(size)


def insert_after(paragraph, text: str) -> Paragraph:
    """在指定段落之后插入正文段落。"""

    new_p = OxmlElement("w:p")
    paragraph._p.addnext(new_p)
    new_para = Paragraph(new_p, paragraph._parent)
    run = new_para.add_run(text)
    set_run_font(run)
    return new_para


def find_para(doc: Document, text: str) -> Paragraph:
    """按完整段落定位锚点，保证补充内容插入到正确小节。"""

    for para in doc.paragraphs:
        if para.text.strip() == text:
            return para
    raise ValueError(f"未找到锚点段落：{text}")


def count_chinese_chars(doc: Document) -> int:
    """统计文本层中文字符数量。"""

    return sum(1 for ch in "\n".join(p.text for p in doc.paragraphs) if "\u4e00" <= ch <= "\u9fff")


